static cv::Mat localNormalization(const cv::Mat &inputgray)
{
    cv::Mat grayf, blur, num, den;
    // convert to floating-point image
    inputgray.convertTo(grayf, CV_32F, 1.0/255.0);
    // numerator = img - gauss_blur(img)
    cv::GaussianBlur(grayf, blur, Size(0,0), 2, 2);
    num = grayf - blur;
    // denominator = sqrt(gauss_blur(img^2))
    cv::GaussianBlur(num.mul(num), blur, Size(0,0), 20, 20);
    cv::pow(blur, 0.5, den);
    // output = numerator / denominator
    grayf = num / den;
    cv::normalize(grayf, grayf, 0.0, 65535.0, CV_MINMAX, CV_16UC1);
    return grayf;
}

